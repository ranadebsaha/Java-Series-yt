class ifelse{
    public static void main(String args[]){
        int a,b;
        a=10;
        b=10;
        if(a>b){
            System.out.println("a is Greater");
        }else if(a==b){
            System.out.println("Both numbers are same");
        }
        else{
            System.out.println("b is Greater");
        }
    }
}